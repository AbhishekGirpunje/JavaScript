const days = 4;

switch(days){
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("tueday");
        break;
    case 3:
        console.log("wednesday");
        break;
    case 4:
        console.log("thursday");
        break;
    case 5:
        console.log("friday");
        break;
    case 6:
        console.log("saturday");
        break;
    case 7:
        console.log("sunday");
        break;

    default:
        console.log("Default")
        break;
}
